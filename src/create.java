    private void uidFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_uidFocusLost
        // TODO add your handling code here:
        String guid=null;
        if(uid.getText()!=null)
            guid=uid.getText();
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/oop?zeroDateTimeBehavior=convertToNull","root","12345");
            Statement s=con.createStatement();
            ResultSet rs=null;
            rs=s.executeQuery("select * from logindata;");
            int f=1;
            while(rs.next())
            {
                if(rs.getString(1).equals(guid))
                    f=0;
            }
            if(f==0)
            {
                check.setVisible(true);
                check.setText("Not Available");
                
                check.setForeground(Color.RED);
            }
            else{
                check.setVisible(true);
                check.setText("Available");
                int r=0,g=0,b=0;
                float[] hsb=new float[3];
                r=14;
                g=140;
                b=214;
                Color.RGBtoHSB(r, g, b, hsb);
                check.setForeground(Color.getHSBColor(hsb[0],hsb[1],hsb[2]));
            }
            
        }
        catch(Exception e)
        {
            System.out.println(e);
        }
    }//GEN-LAST:event_uidFocusLost

